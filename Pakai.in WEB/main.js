    function getProductId() {
      const params = new URLSearchParams(window.location.search);
      const raw = params.get('id');
      const type = params.get('type');
      const idx = raw ? Number(raw) : 1;

      // Map category-scoped ids to unique product ids in `products`:
      // pakaian: 1-16 (no type)
      // tas: 17-32  (type=tas, id 1..16 -> +16)
      // sepatu: 33-48 (type=sepatu, id 1..16 -> +32)
      // jam: 49-64 (type=jam, id 1..16 -> +48)
      if (type === 'tas') return 16 + idx;
      if (type === 'sepatu') return 32 + idx;
      if (type === 'jam') return 48 + idx;
      return idx;
    }

